  public LoginView getView() {
    String username = this.fdUsername.getText().trim();
    String password = new String(this.fdPassword.getPassword()).trim();
    return new LoginView(username, password);
  }

  public void setLoginListener(ActionListener listener) {
    if (this.listenerLogin) {
      this.showErrorMessage("Ya existe una acci√≥n para el bot√≥n de ingresar");
      return;
    }
    this.btnLogin.addActionListener(listener);
    this.listenerLogin = true;
  }

  public void setCancelListener(ActionListener listener) {
    if (this.listenerCancel) {
      this.showErrorMessage("Ya existe una acci√≥n para el bot√≥n de cancelar");
      return;
    }
    this.btnCancel.addActionListener(listener);
    this.listenerCancel = true;
  }

  public void showErrorMessage(String errorNoCredentials) {
    JOptionPane.showMessageDialog(this, errorNoCredentials, "Error", JOptionPane.ERROR_MESSAGE);
  }

  public void showSuccessMessage(String successLogin) {
    JOptionPane.showMessageDialog(this, successLogin, "√âxito", JOptionPane.INFORMATION_MESSAGE);
  }

  public int showConfirmationMessage(String message) {
    return JOptionPane.showConfirmDialog(this, message, "Confirmaci√≥n", JOptionPane.YES_NO_OPTION);
  }
